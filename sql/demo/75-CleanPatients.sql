-- Sync patient and contact names
UPDATE patient p INNER JOIN contact c ON c.id = p.contact_id SET p.gender =
CASE
    WHEN c.title IN ('Mr', 'mr', 'master', 'sir', 'Sir', 'Lord', 'Captain', 'Cptn') THEN 'M'
    WHEN c.title IN ('Mrs', 'Miss', 'Ms', 'Mdme', 'Dame', 'Madame', 'Lady', 'HRH') THEN 'F'
    ELSE 'U'
END;
